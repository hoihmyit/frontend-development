let x = 10;
let y = 5;

// Arithmetic Operators
let addition = x + y;
console.log(addition); //15

let subtraction = x - y;
console.log(subtraction); //5

let multiplication = x * y;
console.log(multiplication); //50

let division = x / y;
console.log(division); //2

// Modulus = Division Remainder (số dư phép chia)
let modulus = x % y;
console.log(modulus); //0

// Exponentiation (lũy thừa)
let exponentiation = x ** y;
console.log(exponentiation); //100000

// Comparison Operators
console.log(x == y); //false
console.log(x != y); //true
console.log(x === y); //false
console.log(x !== y); //true
console.log(x > y); //true
console.log(x < y); //false
console.log(x >= y); //true
console.log(x <= y); //false
